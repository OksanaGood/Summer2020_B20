package Day03Sequences_Variables;

public class Triangle {

    public static void main(String[] args) {

        System.out.println("\t\t\t\t   ^");
        System.out.println("\t\t\t    / \\");
        System.out.println("\t\t\t   /   \\");
        System.out.println("\t\t\t  /     \\");
        System.out.println("\t\t\t /_______\\");

        System.out.println("    ^");
        System.out.println("   / \\");
        System.out.println("  / * \\");
        System.out.println(" / * * \\");
        System.out.println("/ * * * \\");
        System.out.println("---------");
        System.out.println();
        System.out.println();
        System.out.println();
        System.out.println();
        System.out.println();
        System.out.println();
        System.out.println();
    }
}
