package Day03Sequences_Variables;

public class EscapeSequences {

    public static void main(String[] args) {

        System.out.println("Hello Everyone \n\tMy name is Cybertek");
        System.out.println("\tI am in Virginia\n");
        System.out.println("\t\t\t\n\nI love programming");
        System.out.println("\\");// this prints me one back slash\
        System.out.println("\\\\");//this prints me two back slash\\
        System.out.println("/");//this print me one front slash
        System.out.println("//");
        System.out.println("my name is \'Muhtar\'");
        System.out.println("My name is 'Oksana'");
        System.out.println("My name is \"Oksana\"");
        System.out.println("My name is \"Oksana\"");



    }
}
